
x = 5  # (0101 in binary)
y = 3  # (0011 in binary)
# %%

# Bitwise AND 
print(x & y) #0001

# %%
# Bitwise OR
print(x | y) #0111

# %%
# Bitwise XOR 
print(x ^ y) # 1001

# %%
# Bitwise NOT 
print(~x) #1010

# Bitwise Left Shift #x=0101
print(x << 1) #x=1010

# Bitwise Right Shift #x=0101
print(x >> 1) #0010

# Bitwise XNOR
print(~(x ^ y)) 



